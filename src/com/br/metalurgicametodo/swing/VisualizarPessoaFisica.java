        jftCEP.setText(pessoaFisica.getPessoa().getCep());
        SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
        Date d1 = (Date) (pessoaFisica.getDataNascimento());
        String data = format.format(d1);
        jftDataNascimento.setText(data);
        TelefoneDAO teleDAO = new TelefoneDAO();
        telefone = new Telefone();
        telefone = teleDAO.buscarPorCodigoP(pessoaFisica.getPessoa().getCodigoPessoa());
        jftCelular.setText(telefone.getTelefoneCelular());
        jftTelefone.setText(telefone.getTelefoneFixo());
        if (pessoaFisica.getSexo().equalsIgnoreCase("M")) {
            btm_masculino.setSelected(true);
        } else {
            btm_feminino.setSelected(true);
        }
        cb_uf.setSelectedItem(pessoaFisica.getPessoa().getUf());
        cb_estadoCivil.setSelectedItem(pessoaFisica.getEstadoCivil());
        pessoa = pessoaFisica.getPessoa();
        tipoSexo = pessoaFisica.getSexo();

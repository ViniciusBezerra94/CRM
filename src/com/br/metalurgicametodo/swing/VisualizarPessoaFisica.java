    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
         setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        try {
             //salva a nova altera√ß√£o feita na pessoa Fisica
            pessoa.setBairro(txt_bairro.getText());
            pessoa.setCep(jftCEP.getText());
            pessoa.setCidade(txt_cidade.getText());
            pessoa.setEmail(txt_email.getText());
            pessoa.setNumero(Integer.parseInt(txtnumeroCasa.getText()));
            pessoa.setNome(txt_nome.getText());

            pessoa.setUf(String.valueOf(cb_uf.getSelectedItem()));
            pessoa.setRua(txt_rua.getText());
            PessoaDao pDAO = new PessoaDao();
            pDAO.update(pessoa);
            pessoaFisica.setPessoa(pessoa);
            pessoaFisica.setCpf(txt_cpf.getText());
            String dataStr = jftDataNascimento.getText();
            SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
            java.sql.Date data = null;
            try {
                data = new java.sql.Date(format.parse(dataStr).getTime());

            } catch (ParseException ex) {
                Logger.getLogger(Tela_Cadastro.class.getName()).log(Level.SEVERE, null, ex);
            }
            pessoaFisica.setDataNascimento(data);
            pessoaFisica.setEstadoCivil(String.valueOf(cb_estadoCivil.getSelectedItem()));
            pessoaFisica.setRg(txt_rg.getText());
            pessoaFisica.setSexo(tipoSexo);
            PessoaFisicaDAO pfDAO = new PessoaFisicaDAO();
            pfDAO.update(pessoaFisica);
            telefone.setPessoa(pessoa);
            telefone.setTelefoneCelular(jftCelular.getText());
            telefone.setTelefoneFixo(jftTelefone.getText());
            TelefoneDAO telDAO = new TelefoneDAO();
            telDAO.update(telefone);
             setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            JOptionPane.showMessageDialog(null, "Altera√ß√£o do cadastro realizado com sucesso");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao inserir cliente");
        }
        // apos salvar desabilita todos os campos de textos e o bot√£o salvar
        btnSalvar.setEnabled(false);
        txt_nome.setEnabled(false);
        txt_email.setEnabled(false);
        txt_bairro.setEnabled(false);
        txt_cidade.setEnabled(false);
        txt_cpf.setEnabled(false);
        txt_rg.setEnabled(false);
        txtnumeroCasa.setEnabled(false);
        txt_rua.setEnabled(false);
        jftCEP.setEnabled(false);
        jftCelular.setEnabled(false);
        jftDataNascimento.setEnabled(false);
        jftTelefone.setEnabled(false);
        btm_feminino.setEnabled(false);
        btm_masculino.setEnabled(false);
        cb_estadoCivil.setEnabled(false);
        cb_uf.setEnabled(false);
        btnSalvar.setEnabled(false);
         setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
    }//GEN-LAST:event_btnSalvarActionPerformed


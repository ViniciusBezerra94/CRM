    private void btm_gerarRelatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btm_gerarRelatorioActionPerformed
        // TODO add your handling code here:

        Relatorio r = new Relatorio();

        try {
            if (cb_TipoRelatorio.getSelectedItem().toString().equalsIgnoreCase("Selecionar")) {
                JOptionPane.showMessageDialog(null, "Selecione uma opção");
            }

            if (cb_TipoRelatorio.getSelectedItem().toString().equalsIgnoreCase("Clientes")) {
                r.gerarRelatorioClientes();
            }
            if (cb_TipoRelatorio.getSelectedItem().toString().equalsIgnoreCase("Produtos")) {
                r.gerarRelatorioProdutos();
            }
            if (cb_TipoRelatorio.getSelectedItem().toString().equalsIgnoreCase("Vendas")) {
                r.gerarRelatorioVendas();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Tela_Relatorio.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btm_gerarRelatorioActionPerformed


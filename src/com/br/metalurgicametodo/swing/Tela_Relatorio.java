    private void btm_gerarGraficoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btm_gerarGraficoActionPerformed
        Relatorio r = new Relatorio();
        try{
        if (cb_tipoGrafico.getSelectedItem().toString().equalsIgnoreCase("Selecionar")) {
            JOptionPane.showMessageDialog(null, "Selecione uma opção");
        }

        if (cb_tipoGrafico.getSelectedItem().toString().equalsIgnoreCase("Coluna")) {
            r.gerarGraficoBarra();
        }
        if (cb_tipoGrafico.getSelectedItem().toString().equalsIgnoreCase("Pizza")) {
            r.gerarGraficoPizza();
        }
        }catch(SQLException ex){
            Logger.getLogger(Tela_Relatorio.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btm_gerarGraficoActionPerformed


